package net.codestory;

public class Basket {
  long test;
  long back;
  long database;
  long front;
  long hipster;
  long sum;

  public Basket() {
  }

  public Basket(long test, long back, long database, long front, long hipster, long sum) {
    this.test = test;
    this.back = back;
    this.database = database;
    this.front = front;
    this.hipster = hipster;
    this.sum = sum;
  }
}
